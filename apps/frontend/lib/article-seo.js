function normalizeLinkMarkup(value = '') {
  return String(value).trim();
}

function collectWords(article) {
  const parts = [];
  if (Array.isArray(article?.intro)) parts.push(...article.intro);
  if (Array.isArray(article?.sections)) {
    article.sections.forEach((section) => {
      parts.push(section.title || '');
      if (Array.isArray(section.paragraphs)) parts.push(...section.paragraphs);
      if (Array.isArray(section.checklist)) parts.push(...section.checklist);
    });
  }
  if (Array.isArray(article?.faq)) {
    article.faq.forEach((item) => parts.push(item.question || '', item.answer || ''));
  }
  if (article?.cta) parts.push(article.cta.title || '', article.cta.text || '', article.cta.label || '');
  return parts
    .join(' ')
    .replace(/\[[^\]]+\]\([^\)]+\)/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function estimateReadingTime(text) {
  const words = String(text || '').split(/\s+/).filter(Boolean).length;
  const minutes = Math.max(1, Math.round(words / 180));
  return { words, minutes };
}

export function getArticleSeoBlueprint(slug) {
  const blueprint = ARTICLE_SEO_MAP[String(slug || '').trim()];
  if (!blueprint) return null;

  return {
    ...blueprint,
    intro: Array.isArray(blueprint.intro) ? blueprint.intro.map(normalizeLinkMarkup) : [],
    sections: Array.isArray(blueprint.sections)
      ? blueprint.sections.map((section) => ({
          ...section,
          paragraphs: Array.isArray(section.paragraphs)
            ? section.paragraphs.map(normalizeLinkMarkup)
            : [],
          checklist: Array.isArray(section.checklist) ? section.checklist : []
        }))
      : [],
    faq: Array.isArray(blueprint.faq) ? blueprint.faq.map((item) => ({ ...item })) : []
  };
}

export function enhanceArticle(article) {
  if (!article?.slug) return article;

  const blueprint = getArticleSeoBlueprint(article.slug);
  if (!blueprint) return article;

  const visibilityText = collectWords(blueprint);
  const readingTime = estimateReadingTime(visibilityText);

  return {
    ...article,
    focusKeyword: blueprint.focusKeyword,
    metaTitle: blueprint.metaTitle || article.metaTitle || article.title,
    metaDescription: blueprint.metaDescription || article.metaDescription || article.excerpt,
    coverUrl: blueprint.coverUrl || article.coverUrl,
    coverAlt: blueprint.coverAlt || article.coverAlt || article.title,
    intro: blueprint.intro,
    sections: blueprint.sections,
    faq: blueprint.faq,
    cta: blueprint.cta,
    wordCount: readingTime.words,
    readingTimeMinutes: readingTime.minutes
  };
}

export function enhanceArticles(articles = []) {
  return Array.isArray(articles) ? articles.map((article) => enhanceArticle(article)) : [];
}

export function getArticleBreadcrumbItems(article) {
  if (!article) return [];
  const items = [{ label: 'Головна', href: '/' }];
  if (article.category?.slug && article.category?.name) {
    items.push({ label: article.category.name, href: `/categories/${article.category.slug}` });
  }
  items.push({ label: article.title });
  return items;
}
